def setup_fonts():
    global _fonts_ready
    if _fonts_ready:
        return True
    base = os.path.dirname(os.path.abspath(__file__))
    candidates = [
        (os.path.join(base, "fonts", "NotoSansJP-Regular.ttf"),
         os.path.join(base, "fonts", "NotoSansJP-Bold.ttf")),
        (r"C:\Windows\Fonts\meiryo.ttc",  r"C:\Windows\Fonts\meiryob.ttc"),
        (r"C:\Windows\Fonts\YuGothM.ttc", r"C:\Windows\Fonts\YuGothB.ttc"),
        (r"C:\Windows\Fonts\msgothic.ttc", r"C:\Windows\Fonts\msgothic.ttc"),
        ("/usr/share/fonts/opentype/noto/NotoSansCJK-Regular.ttc",
         "/usr/share/fonts/opentype/noto/NotoSansCJK-Bold.ttc"),
    ]
    for regular, bold in candidates:
        if os.path.exists(regular):
            try:
                pdfmetrics.registerFont(TTFont("JFont",     regular, subfontIndex=0))
                pdfmetrics.registerFont(TTFont("JFontBold", bold if os.path.exists(bold) else regular, subfontIndex=0))
                _fonts_ready = True
                return True
            except Exception:
                continue
    return False

# ===== 数秘術計算 =====
def reduce_num(n):
    while n > 9 and n not in (11, 22, 33):
        n = sum(int(d) for d in str(n))
    return n

def life_path(year, month, day):
    return reduce_num(sum(int(d) for d in f"{year}{month:02d}{day:02d}"))

def destiny_num(furigana):
    total = sum(KANA_MAP.get(c, 0) for c in furigana)
    return reduce_num(total) if total > 0 else None

def soul_num(furigana):
    total = sum(DAN_MAP.get(c, 0) for c in furigana)
    return reduce_num(total) if total > 0 else None

def personality_num(furigana):
    dan_total  = sum(DAN_MAP.get(c, 0) for c in furigana)
    kana_total = sum(KANA_MAP.get(c, 0) for c in furigana)
    diff = abs(kana_total - dan_total)
    return reduce_num(diff) if diff > 0 else reduce_num(kana_total)

def birthday_num(day):
    return reduce_num(int(day))

def personal_year_num(month, day):
    return reduce_num(int(month) + int(day) + 2 + 0 + 2 + 6)

def maturity_num(lp, dn):
    return reduce_num(lp + dn) if dn else None

