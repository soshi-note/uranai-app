# ===== 数秘術計算 =====
def reduce_num(n):
    while n > 9 and n not in (11, 22, 33):
        n = sum(int(d) for d in str(n))
    return n

def life_path(year, month, day):
    return reduce_num(sum(int(d) for d in f"{year}{month:02d}{day:02d}"))

def destiny_num(furigana):
    total = sum(KANA_MAP.get(c, 0) for c in furigana)
    return reduce_num(total) if total > 0 else None

def soul_num(furigana):
    total = sum(DAN_MAP.get(c, 0) for c in furigana)
    return reduce_num(total) if total > 0 else None

def personality_num(furigana):
    dan_total  = sum(DAN_MAP.get(c, 0) for c in furigana)
    kana_total = sum(KANA_MAP.get(c, 0) for c in furigana)
    diff = abs(kana_total - dan_total)
    return reduce_num(diff) if diff > 0 else reduce_num(kana_total)

def birthday_num(day):
    return reduce_num(int(day))

def personal_year_num(month, day):
    return reduce_num(int(month) + int(day) + 2 + 0 + 2 + 6)

def maturity_num(lp, dn):
    return reduce_num(lp + dn) if dn else None

def pinnacle_nums(month, day, year):
    m = reduce_num(int(month))
    d = reduce_num(int(day))
    y = reduce_num(sum(int(c) for c in str(year)))
    p1 = reduce_num(m + d)
    p2 = reduce_num(d + y)
    p3 = reduce_num(p1 + p2)
    p4 = reduce_num(m + y)
    return [p1, p2, p3, p4]

def challenge_nums(month, day, year):
    m = reduce_num(int(month))
    d = reduce_num(int(day))
    y = reduce_num(sum(int(c) for c in str(year)))
    def safe(n): return reduce_num(n) if n > 9 else n
    c1 = safe(abs(m - d))
    c2 = safe(abs(d - y))
    c3 = safe(abs(c1 - c2))
    c4 = safe(abs(m - y))
    return [c1, c2, c3, c4]

